#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	*str;

	str = s;
	i = 0;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
	return (str);
}
